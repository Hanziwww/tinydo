export function t(key: string, locale: Locale, params?: Record<string, string | number>): string {
  let str = translations[locale][key] ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      str = str.replace(`{${k}}`, String(v));
    }
  }
  return str;
}
